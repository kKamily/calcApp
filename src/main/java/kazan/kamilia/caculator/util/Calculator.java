package kazan.kamilia.caculator.util;

public class Calculator {
    public static int calculateSum(int num1, int num2){
        int sum = num1 + num2;
        return sum;
    }
    public static int calculateMult(int num1, int num2){
        int mult = num1 * num2;
        return mult;
    }
    public static int calculateSabtrac(int num1, int num2){
        int sab = num1 - num2;
        return sab;
    }

}



